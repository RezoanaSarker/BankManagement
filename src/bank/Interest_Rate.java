    void ShowBalance(){
        totalMoney = Float.parseFloat(jTextField6.getText());
        rate = Float.parseFloat(txtInterest.getText());
        deposit =totalMoney;
        int i=1;
        float k=0;
        int m =Integer.parseInt(txtMonth.getText());
        while(i<=m){
            deposit =deposit+Interest();
            k = k+Interest();
            i = i+1;
            txtAllBalance.setText(""+deposit+"$");
            txtAllInterest.setText(""+k);
            
        }
    }
    
     float Interest(){
        if(deposit<1000) t=(float)(deposit*rate/100);
       else if(deposit<5000) t=(float)(deposit*rate/100);
       else if(deposit<10000) t=(float)(deposit*rate/100);
       else t=(float)(deposit*rate/100);
        return t;
    }
    
    public void Calendar() {
        Calendar cal = new GregorianCalendar();
        int month = cal.get(Calendar.MONTH);
        int year = cal.get(Calendar.YEAR);
        int day = cal.get(Calendar.DAY_OF_MONTH);
        txtDate.setText(+year + "-" + (month + 1) + "-" + day);
    }

