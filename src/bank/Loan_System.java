    private void btnLoanCalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoanCalActionPerformed
        // TODO add your handling code here:
        if (txtLoan.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "You must enter amount of loan to borrow", "Loan System", JOptionPane.INFORMATION_MESSAGE);

        } else {
            if (txtInterest.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "You must enter Interest Rate", "Loan System", JOptionPane.INFORMATION_MESSAGE);

            } else {
                if (txtNumberofPayment.getText().equals("")) {
                    JOptionPane.showMessageDialog(null, "You must enter amount Number of Payment", "Loan System", JOptionPane.INFORMATION_MESSAGE);

                } else {
                    loan = Double.parseDouble(txtLoan.getText());
                    String Balance = String.format(txtLoan.getText());
                    interest = Double.parseDouble(txtInterest.getText());
                    String iInterest = String.format(txtInterest.getText());
                    month = Double.parseDouble(txtNumberofPayment.getText());
                    String iMonth = String.format(txtNumberofPayment.getText());
                    payment = loan + ((loan * interest) / 100);
                    MonthlyPayment = payment / month;
                    String PayMonth = String.format("%.2f", MonthlyPayment);
                    txtMonthlyPayment.setText(PayMonth);
                    name = String.format(txtName.getText());
                    account = String.format(txtGender.getText());
                    account_no = String.format(txtAccountType.getText());
                    txtLoanReport.append("\t\nLoan System:\n\n"
                            + "Customer's Name:\t\t" + name
                            + "\nCustomer's Account No:\t\t" + account_no
                            + "\nCutomer Account Type:\t\t" + account
                            + "\nAmount Of Loan:\t\t" + loan
                            + "\nInterest Rate Of Loan:\t\t" + interest
                            + "\nNumber Of Payment:\t\t" + month
                            + "\nPayment Per Month:\t\t" + MonthlyPayment
                            + "\nTotal Loan Payment:\t\t" + payment
                            + "\nThanks for using i-banking loan system");
                }

            }
        }


    }//GEN-LAST:event_btnLoanCalActionPerformed

