    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String sql = "select * from account where name=?";
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtAccountName.getText());
            rs = pst.executeQuery();
            if (rs.next()) {
                String add1 = rs.getString("name");
                txtName.setText(add1);
                String add2 = rs.getString("acc_no");
                txtAccountNo.setText(add2);
                String add3 = rs.getString("acc_type");
                txtAccountType.setText(add3);
                String add4 = rs.getString("gender");
                txtGender.setText(add4);
                String add5 = rs.getString("adres");
                txtAddress.setText(add5);
                String add6 = rs.getString("balance");
                txtDeposit.setText(add6);
                rs.close();
                pst.close();
            } else {
                JOptionPane.showMessageDialog(null, "Please Enter Correct Account Name.");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

