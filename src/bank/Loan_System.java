    public void Calendar() {
        Calendar cal = new GregorianCalendar();
        int month = cal.get(Calendar.MONTH);
        int year = cal.get(Calendar.YEAR);
        int day = cal.get(Calendar.DAY_OF_MONTH);
        txtDate.setText(+year + "-" + (month + 1) + "-" + day);
    }
    
     public void LoanBalance() {
        String sql = "Insert into loan_balance(name, acc_no, amount, interest, num_pay, mon_pay) values (?,?,?,?,?,?)";
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1, txtName.getText());
            pst.setString(2, txtAccountNo.getText());
            pst.setString(3, txtLoan.getText());
            pst.setString(4, txtInterest.getText());
            pst.setString(5, txtNumberofPayment.getText());
            pst.setString(6, txtMonthlyPayment.getText());
            pst.execute();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

